# Utilizar argumentos dentro de print
print("Mi nombre es", "Python.", end="") # Utilizar end para que coloque un espacio entre esta linea y la siguiente
print("Monty Python.")
print("Mi", "nombre", "es", "Javier", "Pérez", sep="-") # Utilizar sep para agregar - entre cada argumento que imprime esta función
print()
print("Mi", "nombre", "es", sep="_", end="/") # Mezcla de sep y end
print("Javier", "Pérez.", sep="-", end="/\n")
