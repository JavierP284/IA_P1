#Operador de igualdad
print("\nOperador de Igualdad")
var = 0  # Asignando 0 a var
print("La variable es igual a 0: " ,var == 0) #Hacer la pregunta de si es verdadero o falso
var = 1 # Asignando 1 a var
print("El valor de la variable cambia a 1")
print("La variable es igual a 0: " ,var == 0)  #Hacer la pregunta de si es verdadero o falso

#Operador de desigualdad
print("\nOperador de Desigualdad")
print("La variable es igual a 0")
var = 0  # Asignando 0 a var
print("La variable es diferente a 0: " ,var != 0) #Hacer la pregunta de si la variable es diferente a 0 con !=
print("Se cambia el valor de la variable a 1")
var = 1  # Asignando 1 a var
print("La variable es diferente a 0:" ,var != 0) 

#Operador mayor que y mayor o igual que
print("\nMayor que y Mayor o igual que")
